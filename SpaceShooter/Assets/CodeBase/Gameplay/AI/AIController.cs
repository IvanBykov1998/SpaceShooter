using System.Collections.Generic;
using UnityEngine;
using Common;

namespace SpaceShooter
{
    public class AIController : MonoBehaviour
    {
        public enum AIBehaviour // ????? ????????? ????? ? ??
        {
            Null,
            Patrol,
            PatrolRoad
        }

        [SerializeField] private AIBehaviour m_AIBehaviour; // ?????????? ??????? ????? ??????? ????????? ??

        [SerializeField] private AIPointPatrol m_PointPatrol; // ????? ??????????????
        [SerializeField] private List<Transform> m_PointsPatrol; // ?????? ????? ??????????????
        private int m_CurrentPatrolIndex = 0; // ?????? ??????? ?????

        [Range(0.0f, 1.0f)]
        [SerializeField] private float m_NavigationLinear; // ???????? ???????????

        [Range(0.0f, 1.0f)]
        [SerializeField] private float m_NavigationAngular; // ???????? ????????

        [SerializeField] private float m_RandomSelectMovePointTime; // ????? ??? ????????? ??????? (???????????)
        [SerializeField] private float m_FindNewTargetTime; // ????? ???????? ?? ????
        [SerializeField] private float m_ShootDelay; // ??????? ????????
        [SerializeField] private float m_MaxDistValue; // ???????????? ????????? ??????????? ????

        [SerializeField] private float m_EvadeRayLength; // ????? ????????

        private SpaceShip m_SpaceShip; // ?????? ?? ???????
        private Vector3 m_MovePosition; // ??????? ??? ???????????
        private Destructible m_SelectedTarget; // ????????? ????

        [SerializeField] private Projectile m_Projectile; // ?????? ?? ??????

        private Timer m_RandomizeDirectionTimer; // ?????? ????? ????? ????????
        private Timer m_FireTimer; // ?????? ????????
        private Timer m_FindNewTargetTimer; // ?????? ????? ???? ??? ????????

        private void Start()
        {
            m_SpaceShip = GetComponent<SpaceShip>();

            InitTimers();
        }

        private void Update()
        {
            UpdateTimers();

            UpdateAI();
        }

        private void UpdateAI() // ????????? ????????????? ????????
        {
            if (m_AIBehaviour == AIBehaviour.Patrol)
            {
                UpdateBehaviourPatrol();
            }

            if (m_AIBehaviour == AIBehaviour.PatrolRoad)
            {
                UpdateBehaviourPatrol();
            }
        }

        private void UpdateBehaviourPatrol() // ??????????????
        {
            ActionFindNewMovePosition();
            ActionControlShip();
            ActionFindNewAttackTarget();
            ActionFire();
            ActionEvadeCollision();
        }

        private void ActionFindNewMovePosition() // ????? ??????? ??? ???????????
        {
            if (m_AIBehaviour == AIBehaviour.Patrol || m_AIBehaviour == AIBehaviour.PatrolRoad)
            {
                if (m_SelectedTarget != null) // ???????? ? ?????
                {
                    // ???????? ? ???? ?? ??????????
                    Rigidbody2D rb = m_SelectedTarget.GetComponent<Rigidbody2D>();

                    if (rb != null)
                    {
                        float speedSelectedTarget = rb.linearVelocity.magnitude; // ???????? ????
                        float predictionTime = Vector3.Distance(transform.position, m_SelectedTarget.transform.position) / (m_Projectile.Velosity - speedSelectedTarget); // ????? ?? ??????? ?????? ??????? ?? ???? 
                        Vector2 predictedPosition = m_SelectedTarget.transform.position + new Vector3(rb.linearVelocity.x, rb.linearVelocity.y, 0) * predictionTime; // ????????????? ??????? ????

                        m_MovePosition = predictedPosition;
                    }
                    else
                    {
                        m_MovePosition = m_SelectedTarget.transform.position;
                    }
                }
                else
                {
                    if (m_AIBehaviour == AIBehaviour.Patrol)
                    {
                        if (m_PointPatrol != null)
                        {
                            bool isInsidePatrolZone = Vector3.Distance(transform.position, m_PointPatrol.transform.position) <= m_PointPatrol.Radius;

                            if (isInsidePatrolZone == true)
                            {
                                if (m_RandomizeDirectionTimer.IsFinished == true)
                                {
                                    Vector2 newPoint = UnityEngine.Random.onUnitSphere * m_PointPatrol.Radius + m_PointPatrol.transform.position;
                                    m_MovePosition = newPoint;

                                    m_RandomizeDirectionTimer.Start(m_RandomSelectMovePointTime);
                                }
                            }
                            else
                            {
                                m_MovePosition = m_PointPatrol.transform.position;
                            }
                        }
                    }

                    else if (m_AIBehaviour == AIBehaviour.PatrolRoad)
                    {
                        if (m_PointsPatrol.Count > 0)
                        {
                            Transform nextPointPatrol = m_PointsPatrol[m_CurrentPatrolIndex];

                            if (nextPointPatrol != null)
                            {
                                // ?????????, ????????? ?? ??????? ????? ? ??????
                                bool isNearCurrentPoint = Vector3.Distance(transform.position, nextPointPatrol.position) <= m_PointPatrol.Radius;

                                if (isNearCurrentPoint == true)
                                {
                                    // ????????? ? ????????? ?????
                                    m_CurrentPatrolIndex = (m_CurrentPatrolIndex + 1) % m_PointsPatrol.Count;
                                    m_MovePosition = m_PointsPatrol[m_CurrentPatrolIndex].position;
                                }
                                else
                                {
                                    // ????????? ? ??????? ?????
                                    m_MovePosition = nextPointPatrol.position;
                                }
                            }
                        }
                        else
                        {
                            Debug.LogWarning("Next Point Patrol is null.");
                        }
                    }
                    else
                    {
                        Debug.LogWarning("m_PointsPatrol list is empty or null.");
                    }
                }
            }
        }

        private void ActionEvadeCollision() // ????????? ?????????????
        {
            if (Physics2D.Raycast(transform.position, transform.up, m_EvadeRayLength) == true) // ???? ?????????????? ???????? ?? ????
            {
                m_MovePosition = transform.position + transform.right * 45.0f; // ??????? ??????? ?? 45 ????????
            }
        }

        private void ActionControlShip() // ?????????? ?????? ??????? (???????????)
        {
            m_SpaceShip.ThrustControl = m_NavigationLinear;
            m_SpaceShip.TorqueControl = ComputeAliginTorqueNormalized(m_MovePosition, m_SpaceShip.transform) * m_NavigationAngular;
        }

        private const float MAX_ANGLE = 45.0f;
        private static float ComputeAliginTorqueNormalized(Vector3 targetPosition, Transform ship) // ?????? ????????
        {
            Vector2 localTargetPosition = ship.InverseTransformPoint(targetPosition); // ??????? ??????? ? ????????? ??????????

            float angle = Vector3.SignedAngle(localTargetPosition, Vector3.up, Vector3.forward); // ????????? ???? ????? ????? ?????????

            angle = Mathf.Clamp(angle, -MAX_ANGLE, MAX_ANGLE) / MAX_ANGLE;
            // ??????????? ???????? ????????, ???? ???? ?????? MAX_ANGLE - ??????????? ?????? ???? ????????, ???? ??????, ??????? ????? ?????????

            return -angle;
        }

        private void ActionFindNewAttackTarget() // ????? ????? ???? ??? ?????
        {
            if (m_FindNewTargetTimer.IsFinished == true)
            {
                m_SelectedTarget = FindNearestDestructibleTarget();
                m_FindNewTargetTimer.Start(m_ShootDelay);
            }
        }

        private void ActionFire() // ???????? ?? ????
        {
            if (m_SelectedTarget != null)
            {
                if (m_FireTimer.IsFinished == true)
                {
                    m_SpaceShip.Fire(TurretMode.Primary);
                    m_FireTimer.Start(m_ShootDelay);
                }
            }
        }

        private Destructible FindNearestDestructibleTarget() // ????? ????????? ???? ??? ?????
        {
            float maxDist = m_MaxDistValue; // ???????????? ????????? ????????? ?? ????
            
            Destructible potentialTarget = null; // ????????????? ????, ??????? ????? null - ??? ????

            foreach (var v in Destructible.AllDestructibles) // ???? ???????????? ??????? 
            {
                if (v.GetComponent<SpaceShip>() == m_SpaceShip) continue; // ???? ?????? ??? SpaceShip, ????????????? ?? ????????? ??????

                if (v.TeamId == Destructible.TeamIdNeutral) continue; // ???? ?????? ???????????, ????????????? ?? ????????? ??????

                if (v.TeamId == m_SpaceShip.TeamId) continue; // ???? ?????? ??? ???????, ????????????? ?? ????????? ??????

                float dist = Vector2.Distance(m_SpaceShip.transform.position, v.transform.position); // ????????? ?? ????

                if (dist < maxDist) // ????????? ????? ???????? ?????????
                {
                    maxDist = dist;
                    potentialTarget = v;
                }
            }

            return potentialTarget;

        }

        #region Timers
        private void InitTimers() // ????????????? ????????
        {
            m_RandomizeDirectionTimer = new Timer(m_RandomSelectMovePointTime);
            m_FireTimer = new Timer(m_ShootDelay);
            m_FindNewTargetTimer = new Timer(m_FindNewTargetTime);
        }

        private void UpdateTimers() // ?????????? ????????
        {
            m_RandomizeDirectionTimer.RemoveTime(Time.deltaTime);
            m_FireTimer.RemoveTime(Time.deltaTime);
            m_FindNewTargetTimer.RemoveTime(Time.deltaTime);
        }

        public void SetPatrolBehaviour(AIPointPatrol point)
        {
            m_AIBehaviour = AIBehaviour.Patrol;
            m_PointPatrol = point;
        }
        #endregion
    }
}